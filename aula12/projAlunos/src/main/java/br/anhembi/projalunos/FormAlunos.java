    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        String name = txtNome.getText();
        String email = txtEmail.getText();
        
        cleanFields();
        
        Student student = new Student(name, email);
        
        boolean result = StudentDAO.inserir(student);
        
        if(result) {
            txtOutput.setText("Inserido com sucesso!");
            // JOptionPane.showMessageDialog(this, "SUCESSO!!!");
        } else {
            txtOutput.setText("FALHA na inserção!");
        }
    }//GEN-LAST:event_btnInserirActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String StringRA = txtRA.getText();
        // transforme o texto em int
        int ra = Integer.parseInt(StringRA);
        
        Student student = StudentDAO.buscarPorRA(ra);
        
        if(student != null) {
            txtOutput.setText(student.toString());
        } else {
            txtOutput.setText("Não eocontrado");
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String StringRA = txtRA.getText();
        int ra = Integer.parseInt(StringRA);
        String email = txtEmail.getText();
        
        cleanFields();
        
        int result = StudentDAO.atualizarEmail(ra, email);
        
        if(result > 0) {
            txtOutput.setText("Atualizado com sucesso!");
        } else {
            txtOutput.setText("Não eocontrado");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String StringRA = txtRA.getText();
        int ra = Integer.parseInt(StringRA);
        
        int result = StudentDAO.apagarPorRA(ra);
        
        if(result > 0) {
            txtOutput.setText("Apagado com sucesso!");
        } else {
            txtOutput.setText("Não eocontrado");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void cleanFields() {
        txtRA.setText("");
        txtNome.setText("");
        txtEmail.setText("");
    }
    
